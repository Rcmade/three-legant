import { AlertVarientT } from "@/types";
import { create } from "zustand";

interface ResponseMessageState {
  message?: string;
  type?: AlertVarientT;
  setResponseMessage: (message: string, type: AlertVarientT) => void;
}

export const useFormResponseMessage = create<ResponseMessageState>((set) => ({
  message: "",
  type: "default", // default type
  setResponseMessage: (message, type) => set({ message, type }),
}));
